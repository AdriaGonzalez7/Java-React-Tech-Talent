package Tarea4;

public class Tarea4Ejercicio4 {

	public static void main(String[] args) {
		int N = 1;											// Asignamos variables
		System.out.println("Valor inicial de N = " + N);	// Mostramos el valor inicial de N
		N+= 77;												// Le sumamos 77 a N
		System.out.println("N + 77 = " + N);				// Mostramos el nuevo valor de N
		N-= 3;												// Le restamos 3 al último valor de N
		System.out.println("N - 3 = " + N);					// Mostramos el nuevo valor de N
		N*= 2;												// Multiplicamos por 2 al último valor de N
		System.out.println("N * 2 = " + N);					// Mostramos el nuevo valor de N

	}

}
