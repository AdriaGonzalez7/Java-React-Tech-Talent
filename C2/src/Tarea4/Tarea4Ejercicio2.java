package Tarea4;

public class Tarea4Ejercicio2 {

	public static void main(String[] args) {
		int N = 1;
		double A = 2;							// Inicializamos y asignamos variables
		char C = 'a';
		
		System.out.println("Variable N= "+ N +"\nVariable A= "+ A + "\nVariable C= " + C );		// Mostramos las variables
		System.out.println( N + " + " + A + " = " + (N + A));									// Sumamos A + N y mostramos resultado
		System.out.println( N + " - " + A + " = " + (A - N));									// Restamos A - N y mostramos resultado
		System.out.println("Valor Numerico del caracter 'a' = "+(int) C);						// Mostramos el valor C como entero

	}

}
